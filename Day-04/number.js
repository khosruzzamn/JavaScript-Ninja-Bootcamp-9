/*
If you use single or Double quotes your data type turn into a string. Please do not use single or Double Quotes for Number. Write number directly.
*/

// Number Data Type
console.log(123);
console.log(56.202);

// Doing some math using Number Data Type
console.log(10 + 20);
console.log(40 - 20);
console.log(40 / 5);
console.log(5 * 5);
console.log(5 ** 3);

// Modulus or Remainder  Operator
console.log(10 % 2);
console.log(100 % 7);

// Check Number
console.log(typeof 1234);
console.log(typeof 56.202);
